-- 1. Проверка: все ли ингредиенты есть
SELECT * FROM Ингредиенты;

-- 2. Все блюда
SELECT * FROM Блюда;

-- 3. Все заказы
SELECT * FROM Заказы;

-- 4. Количество заказов по сотрудникам
SELECT 
    ID_Сотрудника,
    COUNT(*) AS Количество_заказов
FROM Заказы
GROUP BY ID_Сотрудника
ORDER BY Количество_заказов DESC;

-- 5. Общая выручка за всё время
SELECT 
    SUM(Общая_Сумма) AS Общая_выручка
FROM Заказы;

-- 6. Продажи по дням
SELECT 
    CAST(Дата_Время_Заказа AS DATE) AS Дата,
    SUM(Общая_Сумма) AS Выручка
FROM Заказы
GROUP BY CAST(Дата_Время_Заказа AS DATE)
ORDER BY Дата DESC;

-- 7. Заказы с суммой больше средней
SELECT 
    ID_Заказа AS Номер_заказа,
    Общая_Сумма AS Сумма
FROM Заказы
WHERE Общая_Сумма > (SELECT AVG(Общая_Сумма) FROM Заказы)
ORDER BY Общая_Сумма DESC;

-- 8. Самые дорогие заказы (топ-5)
SELECT TOP 5
    ID_Заказа AS Номер_заказа,
    Общая_Сумма AS Сумма
FROM Заказы
ORDER BY Общая_Сумма DESC;

-- 9. Поставки со статусом 'pending'
SELECT * 
FROM Заказы_поставщикам
WHERE Статус = 'pending';

-- 10. Поставки со статусом 'delivered'
SELECT * 
FROM Заказы_поставщикам
WHERE Статус = 'delivered';

-- 11. Продажи по месяцам
SELECT 
    YEAR(Дата_Время_Заказа) AS Год,
    MONTH(Дата_Время_Заказа) AS Месяц,
    SUM(Общая_Сумма) AS Выручка
FROM Заказы
GROUP BY YEAR(Дата_Время_Заказа), MONTH(Дата_Время_Заказа)
ORDER BY Год DESC, Месяц DESC;

-- 12. Средняя сумма заказа
SELECT 
    AVG(Общая_Сумма) AS Средняя_сумма_заказа
FROM Заказы;

-- 13. Заказы за последние 7 дней
SELECT 
    ID_Заказа AS Номер_заказа,
    Дата_Время_Заказа AS Дата_время,
    Общая_Сумма AS Сумма
FROM Заказы
WHERE Дата_Время_Заказа >= DATEADD(DAY, -7, GETDATE())
ORDER BY Дата_Время_Заказа DESC;

-- 14. Количество заказов по статусам
SELECT 
    Статус,
    COUNT(*) AS Количество
FROM Заказы_поставщикам
GROUP BY Статус
ORDER BY Количество DESC;

-- 15. Блюда с ценой больше 200
SELECT 
    Наименование AS Блюдо,
    Цена_Продажи AS Цена
FROM Блюда
WHERE Цена_Продажи > 200
ORDER BY Цена_Продажи DESC;

-- 16. Ингредиенты с остатком меньше минимума
SELECT 
    Наименование AS Ингредиент,
    Текущий_Остаток AS Остаток,
    Минимальный_Остаток AS Минимум
FROM Ингредиенты
WHERE Текущий_Остаток < Минимальный_Остаток
ORDER BY Текущий_Остаток ASC;

-- 17. Общее количество записей в таблицах
SELECT 'Блюда' AS Таблица, COUNT(*) AS Записей FROM Блюда
UNION ALL
SELECT 'Заказы', COUNT(*) FROM Заказы
UNION ALL
SELECT 'Ингредиенты', COUNT(*) FROM Ингредиенты
UNION ALL
SELECT 'Клиенты', COUNT(*) FROM Клиенты
UNION ALL
SELECT 'Поставщики', COUNT(*) FROM Поставщики
UNION ALL
SELECT 'Заказы_поставщикам', COUNT(*) FROM Заказы_поставщикам
UNION ALL
SELECT 'Позиции_заказов', COUNT(*) FROM Позиции_заказов
UNION ALL
SELECT 'Состав_блюд', COUNT(*) FROM Состав_блюд
ORDER BY Таблица;